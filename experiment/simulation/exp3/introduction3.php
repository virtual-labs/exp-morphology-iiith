Morphology is the study of the way words are built up from smaller meaning bearing units i.e., morphemes. A morpheme is the smallest meaningful linguistic unit. For eg:<br/><br/>  
<ul><li><b>बच्चों</b>  consists of two morphemes, <i>बच्चा</i> has the information of the root word noun "बच्चा" and <i>ओं</i> has the information of plural and oblique case.</li><li><b>played</b> has two morphemes <i>play</i> and <i>-ed</i> having information verb "play" and "past tense", so given word is past tense form of verb "play".</li></ul><br/>


